drop database if exists TORNEOS;
create database TORNEOS;
use TORNEOS;
create table Persona(
	WCAID int not null primary key,
    Nombre_Persona varchar(60)
);
create table Torneo(
	ID_Torneo int not null primary key,
    Nombre_Torneo varchar(40),
    Contra_Torneo varchar(30),
    Fecha_Inicio date,
    Fecha_Fin date
);
create table Categoria(
	ID_Cat int not null primary key auto_increment,
    Nombre_Cat varchar(30),
    Corte int,
    Limite int,
    Horario_Inicio datetime,
    Horario_Fin datetime,
    Zona int,
    ID_Torneo int,
    foreign key (ID_Torneo) references Torneo(ID_Torneo) on update cascade on delete cascade
);

create table Grupo(
	ID_Grupo int not null primary key auto_increment,
    ID_Cat int,
    foreign key (ID_Cat) references Categoria(ID_Cat) on update cascade on delete cascade
);
create table Staff(
	ID_Staff int not null primary key,
    Tipo_Staff int,
    Nombre_Staff varchar(60)
);

create table Participantes_Grupo(
	Id int primary key auto_increment,
	ID_Grupo int,
    WCAID int,
    foreign key (ID_Grupo) references Grupo(ID_Grupo) on update cascade on delete cascade,
    foreign key (WCAID) references Persona(WCAID) on update cascade on delete cascade
);

create table Staff_Grupo(
	Id int primary key auto_increment,
	ID_Grupo int,
    ID_Staff int,
    foreign key (ID_Grupo) references Grupo(ID_Grupo) on update cascade on delete cascade,
    foreign key (ID_Staff) references Staff(ID_Staff) on update cascade on delete cascade
);
